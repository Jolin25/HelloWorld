public class LiteralNumber {
    /** knowledge point:
     *  八进制：
     *      格式： 0
     *  十六进制：
     *      格式：0x（0X）
     */
    public static void main(String[] args) {
        int a = 05;
        int b = 011;
        int c = 0x1;
        int d = 0x11;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
    }
}
